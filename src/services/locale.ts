export class Locale {
  public readonly Market: Market;
  public readonly Language: Lang;
  public readonly MarketAsString: string;
  public readonly LanguageAsString: string;
  public readonly DCatTrail: string;

  constructor(market: Market, lang: Lang, IncludeNeutral: boolean) {
    this.Market = market;
    this.Language = lang;
    this.MarketAsString = market;
    this.LanguageAsString = lang;
    
    const baseTrail = `market=${this.MarketAsString}&languages=${this.LanguageAsString}-${this.MarketAsString},${this.LanguageAsString}`;
    
    if (IncludeNeutral) {
      this.DCatTrail = `${baseTrail},neutral`;
    } else {
      this.DCatTrail = baseTrail;
    }
  }
}
